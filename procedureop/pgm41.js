//array method-filter()
a=[10,11,12,13,14,15]

//display all even numbers
even=a.filter(num=>num%2==0)
console.log(even);
console.log('-----------------------------');
//print all numbers greater than 13
g=a.filter(num=>num>13)
console.log(g);
//array method-some()
b=a.some(num=>num>13)
console.log(b?'true':'false')
















